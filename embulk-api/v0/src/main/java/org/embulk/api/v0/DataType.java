package org.embulk.api.v0;

public enum DataType {
    BOOLEAN("boolean"),
    DOUBLE("double"),
    JSON("json"),
    LONG("long"),
    STRING("string"),
    TIMESTAMP("timestamp"),
    ;

    DataType(final String name) {
        this.name = name;
    }

    public String getName() {
        return this.name;
    }

    @Override
    public String toString() {
        return this.name;
    }

    private final String name;
}
